package section;

public class VaribleDemo3 {

	public static void main(String[] args) {

		char ch1 = 'P', ch2 = 'U', ch3 = 'N', ch4 = 'E';
		System.out.println("Output :: " + ch1 + "\t" + ch2 + "\t" + ch3 + "\t" + ch4 + "\t");

		int no1 = ch1;
		int no2 = ch2;
		int no3 = ch3;
		int no4 = ch4;

		System.out.println("New Output :: " + no1 + "\t" + no2 + "\t" + no3 + "\t" + no4 + "\t");
	}

}
